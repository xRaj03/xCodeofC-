/* Write a program to read 10 integers 
. Display these numbers by printing three numbers in a line seperated by commas */

#include<iostream>

using namespace std;

int main(){

    int a, b, c, d, e, f, g, h, i, j;
    cin>>a>>b>>c>>d>>e>>f>>g>>h>>i>>j;

    cout<<"\n"<<a<<","<<b<<","<<c;
    cout<<"\n"<<d<<","<<e<<","<<f;
    cout<<"\n"<<g<<","<<h<<","<<i;
    cout<<"\n"<<j;


    return 0;
}