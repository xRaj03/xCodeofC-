#include<iostream>

using namespace std;

int main(){

    float num1, num2;

    cout<<"\n Enter the values of num1 and num2 \n";
    cin>>num1>>num2;

    cout<<"\n"<<num1<< "+" <<num2<< " = "<<num1 + num2;
    cout<<"\n"<<num1<< "-" <<num2<< " = "<<num1 - num2;
    cout<<"\n"<<num1<< "*" <<num2<< " = "<<num1 * num2;
    cout<<"\n"<<num1<< "/" <<num2<< " = "<<num1 / num2;

    return 0;
}