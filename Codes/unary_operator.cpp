#include<iostream>

using namespace std;

int main(){

    int num = 3;

    cout<<"The value of num is "<<num<<endl;
    cout<<"\nThe value of num++ is "<< num++;
    cout<<"\nThe new value of num is "<<num;
    cout<<"\nThe value of num-- is "<< num--;
    cout<<"\nThe new value of num is "<<num;
    cout<<"\nThe value of num is "<<num;

    return 0;
}